<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class Event extends Model
{
    protected $fillable = [
        'title',
        'description',
        'event_date',
        'event_time',
        'location',
        'event_type',
        'max_attendees',
        'image_path'
    ];
    
    protected $casts = [
        'event_date' => 'date',
        'max_attendees' => 'integer'
    ];
    
    public function registrations()
    {
        return $this->hasMany(EventRegistration::class);
    }
}